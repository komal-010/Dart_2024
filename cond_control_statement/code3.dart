void main(){
  int x=5;
  if(x==1){
    print("sunday");
  }else if(x==2){
    print("monday");
  }else if(x==3){
    print("tuesday");
  }else if(x==4){
    print("wednesday");
  }else if(x==5){
    print("thursday");
  }else if(x==6){
    print("friday");
  }else if(x==7){
    print("saturday");
  }else{
    print("invalid input");
  }
}